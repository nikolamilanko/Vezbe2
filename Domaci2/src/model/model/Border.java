package model.model;
public class Border {
	private int i;
	private int j;
	private String type;
	
	public Border(int i, int j, String type) {
		this.i = i;
		this.j = j;
		this.type = type;
	}

	public int getI() {
		return i;
	}

	public int getJ() {
		return j;
	}

	public String getType() {
		return type;
	}
	
	
}
